#include "../PowerHealth.h"
#include "../Player.h"
#include <cassert>

void main()
{
   //////////////////////////////////////////////////////////////////
   //Case 1: deal damage to player make sure they take damage
   //////////////////////////////////////////////////////////////////
   Player p = Player(100);
   PowerHealth v = PowerHealth();
   p.TakeDamage(10);
   assert(p.GetHealth() < 100);
   
   //////////////////////////////////////////////////////////////////
   //Case 2: heal player to make sure damage is undone
   //////////////////////////////////////////////////////////////////
   v.ExecutePowerUp(&p);
   assert(p.GetHealth() == 100);

   //////////////////////////////////////////////////////////////////
   //Case 3: powerupt type is 3
   //////////////////////////////////////////////////////////////////
   assert(v.getPowerUpType() == 3);


}