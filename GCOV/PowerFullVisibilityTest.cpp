#include "PowerFullVisibility.h"
#include "Player.h"
#include <cassert>

int main()
{
   //////////////////////////////////////////////////////////////////
   //Case 1: testing that player doesnt have full visiblity
   //////////////////////////////////////////////////////////////////
   Player p = Player(100);
   PowerFullVisibility v = PowerFullVisibility();
   assert(p.GetFullVisibility() == false);

   //////////////////////////////////////////////////////////////////
   //Case 2: testing that player now has full visiblity
   //////////////////////////////////////////////////////////////////
   v.ExecutePowerUp(&p);
   assert(p.GetFullVisibility() == true);

   //////////////////////////////////////////////////////////////////
   //Case 3: testing powerup type equal one
   //////////////////////////////////////////////////////////////////
   assert(v.getPowerUpType() == 1);
   return 0;
}
