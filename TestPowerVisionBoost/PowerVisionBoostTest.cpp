#include "../TheMazeGame/PowerVisionBoost.h"
#include <cassert>

void main()
{
   //////////////////////////////////////////////////////////////////
   //Case 1: player doesnt have vision boost
   //////////////////////////////////////////////////////////////////
   Player p = Player(100);
   PowerVisionBoost v = PowerVisionBoost();
   assert(p.GetVisionBoost() == false);

   //////////////////////////////////////////////////////////////////
   //Case 2: Testing player has vision boost
   //////////////////////////////////////////////////////////////////
   v.ExecutePowerUp(&p);
   assert(p.GetVisionBoost());
}